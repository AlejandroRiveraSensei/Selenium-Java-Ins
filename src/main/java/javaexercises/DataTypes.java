package javaexercises;

public class DataTypes {
    public static void main(String[] args){
        byte diasSemena = 7;
        short estudiantes = 150;
        int poblacion = 1000000;
        long distanciaGalaxia = 9460730472580800L;
        float temperature = 36.6f;
        double pi = 3.14159265358979;
        char letraInicial = 'S';
        boolean aprobado = true;

        System.out.println("Dias de la semana: " + diasSemena);
        System.out.println("Numero de estudiantes: " + estudiantes);
        System.out.println("Poblacion: " + poblacion);
        System.out.println("Discancia a otra galaxa en Km: " + distanciaGalaxia);
        System.out.println("Temparatura corporal: " + temperature);
        System.out.println("Valor de PI: " + pi);
        System.out.println("Letra inicial: " + letraInicial);
        System.out.println("Aprogado?: " + aprobado);
    }

}
